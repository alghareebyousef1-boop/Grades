"""
برنامج تقدير الطالب
يقوم البرنامج بطلب درجة الطالب بشكل متكرر حتى يكتب المستخدم "خروج"
يعرض التقدير: ضعيف، جيد، جيد جداً، ممتاز
"""

def تقدير_الطالب(الدرجة):
    """تحويل الدرجة الرقمية إلى تقدير نصي"""
    if 0 <= الدرجة <= 59:
        return "ضعيف"
    elif 60 <= الدرجة <= 69:
        return "جيد"
    elif 70 <= الدرجة <= 84:
        return "جيد جداً"
    elif 85 <= الدرجة <= 100:
        return "ممتاز"
    else:
        return None  # درجة خارج النطاق


def تشغيل_البرنامج():
    """الحلقة الرئيسية للبرنامج"""
    print("=" * 40)
    print("مرحباً بك في برنامج تقدير الدرجات")
    print("أدخل درجة الطالب (0-100) أو اكتب 'خروج' لإنهاء البرنامج")
    print("=" * 40)

    while True:
        # طلب الإدخال من المستخدم
        مدخل_المستخدم = input("\nأدخل درجة الطالب: ").strip()

        # شرط الخروج من البرنامج
        if مدخل_المستخدم.lower() == "خروج":
            print("شكراً لاستخدام البرنامج. إلى اللقاء!")
            break

        try:
            # تحويل المدخل إلى رقم عشري
            درجة = float(مدخل_المستخدم)
            
            # الحصول على التقدير
            النتيجة = تقدير_الطالب(درجة)
            
            if النتيجة:
                print(f"التقدير هو: {النتيجة}")
            else:
                print("خطأ: الدرجة يجب أن تكون بين 0 و 100.")
                
        except ValueError:
            # التغلب على القيم غير المتوقعة
            print("خطأ: يرجى إدخال رقم صحيح أو كلمة 'خروج'.")

# استدعاء الدالة لتشغيل البرنامج
if __name__ == "__main__":
    تشغيل_البرنامج()
